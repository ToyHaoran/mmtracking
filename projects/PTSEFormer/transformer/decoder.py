class LSTMLikeSENet(nn.Module):  # 对应论文中的门函数G
    def __init__(self, channel, r=16):
        super().__init__()
        self.sigmoid = nn.Sigmoid()
        self.tanh = nn.Tanh()
        self.G = nn.Linear(channel*2, channel)

    def forward(self, q, v):
        b, n, d = q.shape
        QV = torch.cat((q, v), dim=2)  # b 2n d
        M = self.sigmoid(self.G(QV))  # 公式4
        h = M * q + (1 - M) * v  # 公式3的后半部分
        return h